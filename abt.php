          <p>Foundation
            In the early 1990s with the rise of IT sector Prof. Lalith Gamage, who would eventually be the first president of SLIIT recognized the necessity of generating a substantial number of IT professionals in Sri Lanka to fulfill the increasing demand for such expertise. At the time, Prof. Gamage held the position of Senior Lecturer and was also the Director of the Computing Services Centre at the University of Moratuwa. He drafted a proposal for creating an institute of information technology within UoM and forwarded it to Hon. Kingsley T. Wickramaratne, who was the Minister of Internal and International Commerce and Food at that time. In June 1998, the Ministry of Education and Higher Education and the Ministry of Internal and International Commerce and Food jointly brought a proposal to the Cabinet to have the Malabe institute funded by the Mahapola Trust Fund and provided with land. The Chairman of the MTF, who was always the Chief Justice, was consulted on this matter. Although the Cabinet agreed to the project in principle, the Ministry of Finance expressed concerns about the lack of qualified academics to teach at the new institute and the financial burden it would place on the Consolidated Fund.[8][9]
            
            In 1999, Prof. Gamage requested a meeting with Mr. Wickramarathne, the Minister of Internal and International Commerce and Food, to discuss the creation of an institute. The meeting involved Ministry officials, professionals, and academics, and it was proposed to form a company limited by guarantee as an interim measure. This would allow for the project to move forward until cabinet approval was obtained, at which point the Government could take over the Institute and attach it to the University of Moratuwa.
            
            Establishment of the Institute
            Sri Lanka Institute of Information Technology was established in 1999 at BOC Merchant tower, Colombo as a nonprofit company by guarantee with the ability to award Bachelor of Science degrees following amendments to the Universities Act the same year, thus gaining recognition from the Minister of Higher Education.[10][11] SLIIT was established primarily to educate and train information technology professionals. SLIIT was established as an independent institution, as the initial guarantors did not represent any organization and the government or MTF did not have any ownership in it.[12][13]
            
            In September 1999, SLIIT commenced its operations by offering a two-year Diploma Programme at a leased space situated in its Colombo campus. The institute received a staggering 3,150 applications, out of which only 400 were granted admission.
            
            Expansion in to the Malabe Campus
            SLIIT approached the Mahapola Trust Fund to develop the Malabe Campus due to increasing demand for their programmes. Unlike before, this time SLIIT approached MTF as an independent entity rather than a part of UoM. In September 2000, MTF agreed to provide LKR 500 million for construction work and allocated the land in Malabe. Construction started the following year in 2001.
            
            After a change in government in 2001, promised funding from the MTF for the construction of the Malabe Campus stopped abruptly, leaving the MD/CEO to finance the completion of the first phase with his own personal resources. Negotiations with the MTF eventually led to an agreement in 2005 for SLIIT to lease the land and operate independently as a separate legal entity. In exchange, SLIIT would provide board seats for MTF nominees and pay 20% of financial surpluses generated only from the Malabe Campus to the MTF. This agreement remained in effect until another change of government in 2015.[14]
            
            In 2015, the Mahapola Trust Fund requested to review and amend their agreement with the Sri Lanka Institute of Information Technology (SLIIT), asking for the return of all funds originally contributed, including interest. SLIIT had invested over LKR 2 billion into the Malabe Campus between 2005 and 2015. A new agreement was signed in which SLIIT paid LKR 408.5 million to the MTF, which included the initial funding plus interest. The MTF renewed the lease of the land to SLIIT for 60 years, with a lease rental of LKR 20 million per annum, and increasing every five years up to LKR 45 million. The MTF withdrew its members from the board of SLIIT after receiving full payment. The ownership of the land remained with the MTF, and they earn lease rental income from it.
            
            Further Expansion of the University
            In 2007, SLIIT started expanding into new fields of study. These include electronic engineering and business management in collaboration with Sheffield Hallam University.[15] SLIIT also expanded its presence from Colombo and its suburbs to other parts of the country, by establishing centres in Kandy and Matara, making SLIIT accessible in six locations.
            
            In 2011, SLIIT established its Faculty of Business after it was accredited by the UGC to award Bachelor of Business Administration degrees in Human Capital Management, Accounting and Finance, Marketing Management, Quality management and Management Information Systems.
            
            This was followed in 2012, with the establishment of the Colombo Academy of Hospitality Management (CAHM). The project is a joint venture of SLIIT, Colombo Academy of Hospitality Management (CAHM),[16] and William Angliss Institute of Australia. It has been developed in line with international standards, housing a training kitchen, banqueting facility, training restaurant, model bedrooms, an IT training centre, and team rooms for students' practical training to prepare students for the degree of Bachelor of Tourism and Hospitality Management.[17][18]
            
            The Faculty of Engineering was formed in 2013, with it awarding its own Bachelor of Science in Engineering degrees and master's degrees from partner universities such as Curtin University and Sheffield Hallam University.[5] The SLIIT Computing was established as a privately managed subsidiary of SLIIT in Kollupitiya to further expand undergraduate studies.
            
            In 2015, SLIIT established its School of Architecture, offering a three-year degree in architecture. In 2016, introducing 16 new research-based degrees including PhD and MPhil degrees approved by the Ministry of Higher Education, Technology and Innovation of Sri Lanka.[19]
            
            In 2017, the Faculty of Humanities and Sciences was established as the newest division that provides the most extensive assortment of programs offered by any faculty on the campus. This faculty offers programs in diverse areas, including Biotechnology, Education, Law, Mathematics, Nursing, and Psychology. </p>
